export function add(args1: number, args2: number) {
  return args1 + args2;
}

export function subtract(args1: number, args2: number) {
  return args1 - args2;
}

export function multiply(args1: number, args2: number) {
  return args1 * args2;
}

export function divide(args1: number, args2: number) {
  return args1 / args2;
}
